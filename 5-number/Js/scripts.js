/* O number é o tipo de dado para numeros;
Possui 3 valores simbolicos: +Infinity, -Infinity e Nan;
Não existe um tipo definido para inteiros, todo numero é Number;
*/

var numero = 5;

console.log(numero);
console.log(typeof numero);

var float = 5.32;

console.log(float);
console.log(typeof float);

var textoComNumero = '523';

console.log(textoComNumero);
console.log(typeof textoComNumero);

console.log(NaN);
console.log(typeof Nan);
console.log(typeof +Infinity);
console.log(typeof -Infinity)
